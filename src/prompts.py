SYSTEM_ADS = """Ты — специалист по таргетированной рекламе ВКонтакте.

На основе структурированных маркетинговых данных создай
готовые рекламные объявления.

Требования:
- 2-4 варианта объявлений
- Каждый вариант для отдельного сегмента аудитории
- Если в запросе есть пожелания пользователя по кампании — строго учитывай их в текстах, тоне и акцентах объявлений
- Короткий заголовок (до 60 символов)
- Основной текст (до 300 символов)
- Четкий CTA
- Без клише
- Без чрезмерных обещаний
- Подходит для формата "Запись в сообщения"

Ответ строго в JSON:

{
  "ads": [
    {
      "segment_name": "...",
      "headline": "...",
      "body_text": "...",
      "cta": "...",
      "visual_concept": "...",
      "image_prompt_short": "краткое описание для картинки"
    }
  ]
}
"""


def build_user_ads(analysis_json: str, user_wishes: str | None = None) -> str:
    parts = [
        "Маркетинговые данные:",
        "",
        analysis_json,
        "",
    ]
    if user_wishes:
        parts.extend([
            "Пожелания пользователя по кампании (обязательно учти при создании объявлений):",
            user_wishes,
            "",
        ])
    parts.append("Создай рекламные объявления с учётом пожеланий пользователя.")
    return "\n".join(parts)


